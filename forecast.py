load_dotenv(f"{dir}/.env")
chatbot = chatgpt.ChatBot(api_key = os.environ.get("OPENAI_API_KEY"))

with open(f"{dir}/system_setting.txt", "r", encoding="utf-8") as f:
    system_setting = f.read()

chatbot.set_system_setting(system_setting) #ChatGPTに最初の命令文を設定する

while True:
    while True:
        prompt = input("お住いの地域を入力してください：")
        message = chatbot.chat("地名：" + prompt)
        if message == "0":
            print("該当する地名が日本国内に存在しません。もう一度入力しなおしてください。")
        else:
            break


    url = f"https://weather.tsukumijima.net/api/forecast/city/{message}"

    try: 
        data = requests.get(url).json() # json形式でデータを取得する
        #print(data)

        print("-" * 40)
        for forecast in data['forecasts']:
            date_label = forecast['dateLabel']       # 今日、明日など
            date = forecast['date']                  # 日付
            weather = forecast['detail']['weather']  # 天気の詳細
            wind = forecast['detail']['wind']        # 風
            temp_min = forecast['temperature']['min']['celsius']
            temp_max = forecast['temperature']['max']['celsius']
            rain = forecast['chanceOfRain']          # 時間帯ごとの降水確率

            print(f"{date_label}（{date}）の天気予報：")
            print(f"  天気　　：{weather}")
            print(f"  風　　　：{wind}")
            print(f"  最高気温：{temp_max if temp_max else '不明'} ℃")
            print(f"  最低気温：{temp_min if temp_min else '不明'} ℃")
            print(f"  降水確率：")

            for time_range, prob in rain.items(): #
                print(f"    {time_range.replace('T', '').replace('_', '～')} (時)：{prob}")
            print("-" * 40)



        break
    except Exception as e:
        print(f"想定外のエラーが発生しました。 {e}\nもう一度適切な地名を入力してください。")



print("プログラム終了")